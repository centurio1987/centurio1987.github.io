내 한계가 어디인지 가늠할 틈 없이 전투를 치르다 보니, 자연스럽게 한계를 넘나들었고, 스트레스 한계치를 계속해서 자극 했다. 점차, 스트레스 내성이 높아지고, 집중력이 높아지고, 일의 밀도가 높아지고, 디테일이 상승 했다. 폭 넓게 흩어져 있던 지식들도 실무와 함께 녹아들어 나와 진정한 하나가 되었다. 실무 경험이 부족해서 늘 초조 했던, 코딩도 직접 구현에 더해, 가이드 코드, 스켈레톤까지 정신없이 짜서 공유하고, 팀원들과  코드 리뷰하고, 투명하게 피드백 주고받으며, 디테일들을 더욱 잘게 쪼갤 수 있게 되면서, 숨쉬듯 편안해 졌다. 

