“안녕하세요. 이렇게 다시 뵙게 되네요.”

대표님은 많이 차분해 보였다. 사람을 투시하듯 쨍한 안광도 가라앉아 있었다. 

“요즘 책을 보는데, 수파리(守破離)라는 단어가 나왔어요. 윤덕님 이력서에서 처음 본 단어 였어서, 보는 순간 윤덕님 생각이 났습니다.”

“하하 재밌는 일이 다 있네요.”

세상 인연이란게 예측하기 어려운 부분이 있지만, 다소 삭막한 인생을 사는 나에게는 꽤나 생소한 계기였다. 진짜로 하고 싶으신 얘기는 따로 있으실 거라 생각 했으나, 마침, 인생사도 무료한 김에, 재밌는 이벤트라 생각해서 쭉 들어보고자 하는 호기심이 동했다.

“최근 회사에서 제 방식에 대한 큰 실패를 느끼고, 내가 많이 부족하구나 라는 생각이 들었습니다.”

“한치의 의심 없는 자신감으로 살다가 갑자기 그런 감정을 확 느끼니 정신적으로 충격이 왔고, 휴식하면서, 삶의 방식에 대해 반성을 하게 되었습니다.”

“그런 와중에 책을 보면서 수파리라는 단어가 나왔고 윤덕님이 예전에 해주셨던 말이 다시금 생각이 나서 연락 드리게 되었습니다.”

당연하지만, 대표님과는 처음 보는 것이 아니었다. 이 날로부터 거의 1년 전, 직장을 구하고 있었고, 이 때도, 대표님께 먼저 제안이 왔었다. CTO로 합류 의사를 물어 보셨고, 나는 오케이를 했다. 한 달 이후에 입사 하는 것으로 이야기가 됐다. 그러나 사람에게 크게 기대하고 확신하시는 느낌이 들었고, 아직 퇴사 전이라, 업무 시간 임에도 많은 메시지들이 도착해서, 아무래도 부담스럽다 말씀드리고 정중히 입사 제안을 거절드리게 되었다. 당시의 나는 내 성향에서 벗어나는 일에 대해 잘 대처할 자신이 없었다.

그러나, 지금의 대표님은 많이 침착해 보였다. 내가 거절하며 말씀드렸던 내용이, 최근의 실패 과정에서도 맞 닿는 부분이 있으셨던 모양이었다. 그래서 다시 만나보고 싶은 생각이 드셨던 모양이다.

그 날은, 그렇게 서로의 근황을 주고 받으며 헤어졌고, 그 이후에도 나의 제안으로 다시 한 번 보게 되었다. 그 때 못 다한 근황 이야기를 조금 더 하며 분위기가 무르익자, 대표님께서 말씀하셨다.

“지금 깨달음을 얻은 저라면, 윤덕님과 잘 해나갈 수 있을 것 같습니다. 어떻게 생각하시나요?”

다시 같이 일하기를 제안 해 주셨고, 나는 즉답을 하지는 못했다. 스스로 동력이 많이 떨어져 있다고 느꼈고, 확신 없이 들어가서, 무기력한 모습을 보인다면, 너무 죄송할 것 같기 때문이었다. 사실, 나는 같이 일하는 사람들과 합이 잘 맞을 때, 최대의 행복과 업무의 보람을 느낀다. 좋은 사람들과 2인3각 하며 성과를 이루었을 때의 보람은 이루 말할 수 없다. 그래서 며칠 후, 나도 대표님께 제안을 드렸다.

“지금 일 하고 계신 개발자 분들을 한 번 만나 뵐 수 있을까요?”

“물론이죠!”

대표님께서는 요청에 응해 주셨고, 얼마 후, 만남이 성사 되었다.

그 자리에는, 이미 리드를 하고 계신 개발자 팀원 B님과 주니어 개발자 팀원 C님, 당시 PO를 하시던 코파운더 겸, 이사님 께서 앉아 계셨다. 나 한테 중요한 건 같이 일하는 사람의 태도 였기 때문에, 그 분들의 분위기와 표정, 말씀하시는 방식 등을 살폈고, 같이 일하면 뒤를 걱정 하지 않고, 전력을 투사 했을 때, 같이 성과를 낼 수 있고 보람을 느낄 수 있는 팀이라는 확신이 섰다. 그렇게 새로운 여정이 결정 되었다.

---

“독일에 출장을 가서 소개하고 싶으니 4주 안에 SaaS 만들어 주세요.”

“… 알겠습니다.”

입사 이후, 얼마 후 부터는 리더십 회의라는 명목으로, 대표님과 이사님, 내가 정기적으로 외부에서 만났다. 그 ‘얼마 후’ 라고 하는 표현이 들어간 이유는, 애매모호한 시기가 있었기 때문이다. 원래 리드를 하셨던 개발자 분은, 좋은 분을 모시게 되어 안심하고 떠날 수 있게 되었다며 곧 나가 셨고, 나도 공교롭게 협심증이 생겨, 급하게 수술 받게 되었으며, 나랑 같은 시기에 입사 하신 개발자 분도 계셨다. 이 분이 팀원 A다. 나름대로 입사한 날부터 팀빌딩을 위해 소통을 했으나, 아직은 충분히 유대감이 쌓이지 않은, 그런 시기였다.

대표님의 4주 프로젝트 선언 이전에, 리더십 회의를 하면서, 초반에 불협화음이 꽤 많았다. 자세히 이야기 하기는 어렵지만, 내가 다른 회사들에서 느낀 것들이 좀 더 급진적으로 이루어지고 있는 느낌이었고, 나는 의미 있는 주제와 의사결정 과정이 이루어지길 요청드리며, 난항이 있었다. 그러면서도, 서로의 성향을 파악하게 되면서, 적어도 회의를 진행은 할 수 있는 상태로 되어가는 와중이었다.

그런 어느 날, 4주 프로젝트 선언이 있으셨고, 어느 범위까지 대화가 가능할지 가늠이 가지 않는 상황에서, 적극적으로 임하기 보다는, 당장 회의의 평화를 선택하는 것이 안전하다고 생각했다. 적어도 당시에는. 개발 리드로 입사한지 얼마 안됐고, PO는 이사님이 하고 계셨으므로, 잠재적 갈등 위험을 만들기 보다, 믿고 내 일에 집중하는 것을 택했다. 신뢰를 드리기 위해 결과로 보여주면, 이후 상황이 더 좋아질 것이라는 생각도 염두에 두었다.

아직 유대감을 쌓지 못한 3명의 개발자와 한 명의 디자이너, 그리고 ‘LCA 분석 SaaS’라는 대명제의 아이디어 겸 기획이 남겨졌다. 사실, 프로젝트 시작 후 일주일 뒤, 개발자 한 분이 더 들어오셨다. 일명 팀원 D님. 우리는 최대한 회사에 계신 LCA 컨설턴트 분께 요청 드려, 생각 나는 대로 질문 하고, LCA 관련 문서와 전공서를 읽었으며, 컨설턴트께서 학습 세션을 마련해 주셔서, 경청하기도 하였다.

우리에게 주어진 시간은 4주이므로, 어쩔 수 없이 도메인을 이해하는 시간은 투자 할 수 밖에 없었지만, 시나리오를 만들고 적용하기에는 시간이 촉박하다고 판단 했다. 위험을 무릅쓰고, 직관과 본능에 의존해, 학습한 내용을 토대로, 바로 디자인으로 옮겨 냈다. 그 과정에서, 생각 할 수 있는 시나리오를 무수히 돌려 보고 외삽하며, just in time으로만 모순을 찾아서 개선했다.

그렇게 하고 나니 남은 시간은 2주. 우리는 코딩에 돌입했다. 협업이다 보니까, 코딩 하면서, 생각 나는 만큼은 그때 그때 컨벤션을 정의 했으며, 코드베이스 구조를 합의 했다. 또한, 코드 리뷰를 통해 서로의 코드 스타일을 이해하고 일치 시켜 나가면서 작업 했다. 꽤 시간이 투자 되는 작업이었으나, 유지 보수를 위한 최소한의 안전 장치가 없다면 수습이 어려운 프로젝트로 전락할 수 있기에, 과감히 투자 했다. 다소의 공수를 증가 시키더라도 팀원들이 성장을 위한 경험이 될 수 있는 프로젝트가 되길 바란 부분도 있었다.

LCA로직이 포함되는 서비스는 직관과 본능에 의지해서만 만들기에는 점점 모순점들이 드러났고, 화면에 다소 복잡한 형태의 그래프를 페인팅 해야 했기에, 기본적으로 속도가 빠르게 진행되기는 어려웠다. 하물며 팀원 D님은 오자 마자 환영이나 적응 없이, 코딩 부터 해야 했다. 입소하자마자 낙하산을 메고 전장에 투입 되는 꼴이었다.

이미 84시간 정도는 투자 하고 있던 상황에서, 여전히 제 시간에는 끝날 기미가 보이지 않자, 우리는 아예 회사에서 숙식 하며 소위 크런치를 하기로 결정 했다. 그러다보니 팀원 A님은 해 뜨는 걸 보며 같이 생일을 맞이 헀고, ‘크런치 후에는 버거킹’이라는 아는 사람만 아는 사내 밈이 생긴 것이었다. 그렇게, 다소 버그가 남아있긴 하지만, 사용 가능한 MVP를 만들었고, 끝난 기념으로 교외에 나가서 식사도 하고, 커피도 마시면서 회고 하는 시간을 가지기도 했다. 이후에는 감정적인 소회를 남기는 회고가 거의 없었지만, 이 때는 아직 서로를 모르는 상태에서 100시간 넘게 크런치를 하고, 심리적으로 예민해 지는 부분까지 더해지다 보니, 사소한 오해 정도는 있었고, 잘 풀어내면서, 앞이 보이지 않던 프로젝트를 힘을 합쳐서 잘 끝냈다는 것에 해방감을 느낄 수 있었다. 

지금 생각하면 많은 부분이 미숙하고 힘들었던 경험이다. 하지만, 상황이 우리를 이렇게 만들어 주지 않으면, 많은 이해관계가 얽힌 회사라는 공간에서, 동료들과 전우애를 쌓기는 쉽지 않았을 것이다. 역경이 우릴 죽게 하지 않았으니, 그대로 양분이 되어 주었다 말 할 것이다.

---

이 시기까지 회사는 LCA 시장에서 경쟁력 있는 SaaS를 만든다는 심플한 명제를 가지고 있었다. 하지만 이 이후로는 비즈니스나 경영이나 의사결정의 과도기를 거쳤다. 컨설팅 쪽 영업을 확장하거나, 탄소 영역의 아웃 소싱을 노리기도 했다. 시장의 반응에 따라 많은 것들이 결정 되는 스탠스였기 때문에, 여러 계약에 의해 프로젝트를 동시에 진행해야 하는 복잡한 경우도 생겼다. 계약이 없을 때도 제품팀은 계속 바쁘게 돌아갔다. 결국 회사에서 이상적으로 생각 하는 것은 LCA 분석 서비스를 통한 가치 창출이었다. 그래서, 나는 기획부터 최대한 자원을 투자해서 다시 시작하기를 간곡히 요청 했고, 받아들여 졌다. 이상적인 것과 별개로, 우선순위는 가장 낮았다. 계약이 체결되면, 고객과의 약속이 최우선이 되기 때문에, LCA 프로젝트는 후순위가 될 수 밖에 없었기 때문이다. 그래서, 계약의 공백일 때도 인텐시브 하게 진행 됐으며, 계약이 있을 때도, 업무 시간을 할애 해서 계속 진행하는 것이 방법이었다. 혹여라도 계약이 많아지게 되면, LCA 프로젝트에 가용할 수 있는 자원이 고갈 상태가 될 수 있기 때문이다.

PO를 겸임하고 계시던 이사님은 사실, LCA 컨설턴트와 고객 성공 매니저역을 주로 하셨고, IT나 제품에 대한 전문가는 아니셨다. 그리고, 자주 외부 미팅과 출장이 있으셨고, 고객이 직접 연관되어 있는 그런 업무들에 비해, 제품 업무는 후순위가 될 수 밖에 없었다. 그래서, 나는 최대한 나의 역할 안에서 좋은 제품이 나오기 위해 필요한 것과 지식에 대한 의견을 공유 드렸다.

제품팀에서도 앞으로 LCA 분석 서비스를 관리하고 확장하기 좋은 기반을 만들기 위해 여러 준비를 했다. 프로세스를 우리 조직 환경에 맞게 조정하고, 노션을 통해 프로세스를 지원할 수 있는 환경을 구축했다. 개발 원칙과 준수 사항, 회의 방식과 목적, 팀이 추구해야 하는 가치와 그 우선순위들 같은 IT 거버넌스와 이하 프로세스들을 설계 했다. 코드 베이스 구조를 논의하여 정하고, 어떤 기술을 쓸지를 정헀다. DDD를 적용하기로 결정했다. 업스트림 산출물을 보고, 도메인 전문가인 컨설턴트 분들과 많은 소통과 질문을 하면서, 도메인 모델을 설계 하고, 개발에 필요한 단서들을 정리 했다. 디자인 시스템을 만들고 아토믹 시스템을 통해 컴포넌트를 관리 하기로 했다. 아직은 그 디테일과 성숙도가 부족하지만, 문제는 없었다. 핵심은 수행을 통한 개선과 성숙도 향상에 있었기 때문이다.

사전작업들이 어느 정도 마무리 됐을 때, 우리는 본격적으로 개발에 착수 했다. 이번에 처음 시도한 것들이 많았기 때문에, 디테일 공백에 직면 했을 때, 판단하고, 적합하지 않은 부분이 있으면 조정해 가면서 진행했다. 그래서 매일매일이 변수였고, 아직은 공수 예측이 정확하지 않은 과도기적 단계를 겪었다. 하지만, 직면한 문제들이 해결 가능한 것들이었기 때문에, 프로젝트는 계속 진행되어 갈 수 있었다.

그러던 어느날 새벽 1시, 팀원 D님과 야근 중이었다.

“윤덕님, 요청이 너무 느려서 살펴 보니, 쿼리 한 번에 로우가 3억개 쯤 찍힙니다.”

아차 싶었다. 내가 이번 프로젝트에서 정한 방침은, 우리가 아직 성숙도가 부족한 만큼 도메인 모델링도 이론을 최대한 준수 하고, 아키텍쳐도 DDD를 우선순위로 설계하는 것이었다. 그 과정에서 DB 스키마는 도메인 모델과 동일하게 가져갔다. ORM을 사용하는 만큼, 아주 자연스러운 결정으로 보여졌다. 영속성 레이어에 대한 특별한 고려 사항 없이 ORM의 기능에 의존하고자 했다. 그러면서도 RDB의 활용 잠재성을 확보 하기 위해, 필드를 가급적 JSON으로 저장하기 보다, 테이블로 각각 정의하고자 했다. 하지만, 그러다 보니, 특정 aggreate root에 해당하는 테이블을 조회 하는 경우, 1:N 관계를 가지는 테이블의 중첩도가 높아 질 수록, 비약적으로 로우 수가 늘어나는 것이었다.

설계 컨셉으로 인해 생긴 문제였기 때문에, 단순히 버그 수정의 영역으로 치부할 수 없었다. 머릿속으로 비상을 외치며, 두뇌 가동에 모든 정신을 집중 시켰다. 당시 분석 결과는 스키마 설계와 도메인 설계를 분리하지 않은 것이었고, 당장의 해결 방법은 aggregate root가 가지고 있는 프로퍼티를 json으로 저장하는 것이었다. 지금 생각하면 어떤 접근법을 사용하느냐에 따라, 진단과 해결책이 여럿 존재할 수 있다. 당시의 진단 처럼 DB스키마 설계를 별도로 고려 하는 방법이 있다. 충분치 않다면, 그에 더하여, ORM이 제공하는 API에만 의존하지 않고, 리퀘스트에 따른 쿼리를 작성할 수도 있다. 아예 아키텍쳐 단위에서 cqrs를 적용한다면, 일관성 있는 해결책을 강제할 수 있다. 오히려, ORM 기능을 적극 활용하여, eager 옵션 해제 등, 조인을 제한하는 방법으로 해결 할 수도 있다. 꼭, 방대한 데이터를 가지고 있는 엔티티를 한 번에 다뤄야 한다면, 영속 레이어에서는 JSON으로 저장하고, 통신 과정에서는 동기화 알고리즘과 동반한 기술을 적용하여, 기술 설계 및 구현 할 수 있다. 제품 디자인부터 권한이 있다고 한다면, 도메인 모델링과 제품 디자인을 교차 수행 하면서, 구현 가능성을 고려한, 상호 보완적 설계를 가져가는 방법이 있다. 너무 방대한 aggregate가 있다면, 역으로 컨셉을 조정하는 식이다. 지금이라면 JSON 방식으로 해결하더라도 명확한 컨셉과 명분을 토대로 해결 가능 했겠지만, 당시엔 해당 케이스에 대한 미봉책에 불과했다.

어느 순간 부터는 탄소 회계 관련 아웃 소싱 프로젝트와 동시에 진행 되기도 하면서, 난이도가 상승 했으나, 업무가 어려워 지는 만큼, 우리도 성장을 통해, 난관을 극복 하고, 프로젝트를 마무리 지을 수 있었다.

