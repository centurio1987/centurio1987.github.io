그렇게, 상황에 대한 원망이 들었다. 왜 이런 의사결정들이 생기는지도 곰곰히 생각해 봤다. 겉으론 청운의 꿈을 안고 스타트업을 키워보고자 하는 낭만가들이라고 생각이 들었다. 그러나 반복된 조급함으로 인한 실패로 그들이 지쳐가는 것을 보고, 나중에서야 이해한 것이 있다. 대부분은 경영을 통해 회사를 점차 키워나가는 게 아닌, 단기 임팩트로 빠르게 엑싯 하는 한 방 역전의 꿈을 꾸는 사람들이었다는 것을.

