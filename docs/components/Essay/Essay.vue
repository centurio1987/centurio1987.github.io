    currentPage.value = Math.max(0, totalPages.value - 1);
    tempPage.value = currentPage.value;
  }
};

// 모드 전환 (페이징 <-> 스크롤)
const toggleMode = async (): Promise<void> => {
  isScrollMode.value = !isScrollMode.value;
  if (!isScrollMode.value) {
    await nextTick();
    updateLayout();
  }
};

// 1페이지 <-> 2페이지 전환 (현재 읽는 위치 보존 수학적 보정)
const togglePageMode = async (): Promise<void> => {
  const newMode = !isTwoPageMode.value;

  // 순수 텍스트 페이지 번호 계산 (표지가 있으면 1을 뺌)
  let currentTextPage = hasCover.value
    ? Math.max(0, currentPage.value - 1)
    : currentPage.value;

  // 단수 변경에 따른 스케일 보정
  if (newMode) {
    currentTextPage = Math.floor(currentTextPage / 2); // 1단 -> 2단
  } else {
    currentTextPage = currentTextPage * 2; // 2단 -> 1단
  }

  // 표지 오프셋 복구
  let newCurrentPage = (hasCover.value ? 1 : 0) + currentTextPage;

  // 단, 모드 변경 직전 화면이 표지(Page 0)였다면, 그대로 표지에 머무름
  if (hasCover.value && currentPage.value === 0) {
    newCurrentPage = 0;
  }

  currentPage.value = newCurrentPage;
  isTwoPageMode.value = newMode;
  tempPage.value = currentPage.value;

  await nextTick();
  updateLayout();
};

const changeFontSize = async (delta: number): Promise<void> => {
  const newSize: number = fontSize.value + delta;
  if (newSize >= 12 && newSize <= 40) {
    fontSize.value = newSize;
    if (!isScrollMode.value) {
      await nextTick();
      updateLayout();
    }
  }
};

// ----------------------------------------------------------------------
// 4. 네비게이션 제어
// ----------------------------------------------------------------------
const prevPage = (): void => {
  if (currentPage.value > 0) {
    currentPage.value--;
    tempPage.value = currentPage.value;
  }
};

const nextPage = (): void => {
  if (currentPage.value < totalPages.value - 1) {
    currentPage.value++;
    tempPage.value = currentPage.value;
  }
};

const toggleMenu = (): void => {
  isMenuVisible.value = !isMenuVisible.value;
};

const handleContainerClick = (): void => {
  if (isScrollMode.value) toggleMenu();
};

const onSliderInput = (): void => {};
const onSliderChange = (): void => {
  currentPage.value = tempPage.value;
};

const handleKeydown = (e: KeyboardEvent): void => {
  if (isScrollMode.value) return;
  if (e.key === "ArrowLeft") prevPage();
  if (e.key === "ArrowRight") nextPage();
};

let resizeObserver: ResizeObserver | undefined;

onMounted(() => {
  if (typeof ResizeObserver !== "undefined") {
    resizeObserver = new ResizeObserver(() => {
      if (!isScrollMode.value) updateLayout();
    });
    if (containerRef.value) resizeObserver.observe(containerRef.value);
    if (columnsRef.value) resizeObserver.observe(columnsRef.value);
  }

  updateLayout();
  window.addEventListener("keydown", handleKeydown);
});

onUnmounted(() => {
  if (resizeObserver) resizeObserver.disconnect();
  window.removeEventListener("keydown", handleKeydown);
});

watch(rawText, async () => {
  if (!isScrollMode.value) {
    await nextTick();
    updateLayout();
  }
});
</script>

