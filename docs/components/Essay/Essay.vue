뒤죽박죽 엉켜버린 감정으로 공부에 크게 집중이 되진 않았으나, 할건 해야 했다. 여기서는 기술 보다는 다른 쪽으로 눈을 많이 돌렸다. 프로젝트와 프로세스론, 마케팅, 경영, 경제를 공부했다. 내가 추구하는 방향성 안에서, 이것들이 소프트웨어를 이해하는데 결국 도움을 줄것이란 생각이었다. 결국, 도움이 되었다. 소프트웨어 그 자체보다는 비즈니스 관점에서 제품으로써의 소프트웨어를 어떻게 바라봐야 하고, 개발자가 바라보는 개발이란 그라운드를 넘어, 프로젝트, 프로세스, 그리고 가치 사슬에 이르기 까지, 가치 창출의 전 과정을 이해하는 마중물이 되었다.

물론 개발 기술도 계속 연구 했다. 딥러닝 때문에 주목도가 높아진 파이썬, 당시의 핫한 주제였던 클라우드 컴퓨팅 기술들, js와 node.js, 그리고 조금 새로운 개념이었던 angular.js 같은 웹 프레임워크 기술과 webpack 같은 번들러. 깊이 보다는 폭을 넓히는데 여전히 집중하던 시기였다. 

하지만, 무엇보다도 딥러닝에 집중했다. 앞으로는 딥러닝의 시대가 올 것이란 확신이 있었고, 개발자가 아닌, 데이터 분석가가 되기로 결심했다. 지금 생각하면 트렌드 예측과 별개로, 딥러닝을 너무 쉽게 보고 접근 했다고 생각한다. torch나 tensorflow를 사용해서 무작정 따라해 보기도 하고, 논문을 보고, 고전적인 머신 러닝 모델도 공부 했으나, 무엇 하나 이해하는 것이 없었다. 졸업 할 때 즈음 깨달은 것은, 딥러닝을 하기 위해선 펀더멘털이 되는 기저의 지식들을 먼저 이해해야 한다는 것이었다.

어쨌든, 데이터 분석가가 되기로 결심한 이상, 딥러닝에 대한 공부를 일단락을 짓고 싶었다. 완전한 수포자였기 때문에 중학교 수학부터 다시 공부 했고, 유튜브와 블로그를 뒤져 가면서 궁금한 내용들을 채워나갔다. 그리고 선형대수학, 다변량 미적분학, 통계학을 K-mooc를 통해 학습했다. 공부 노하우는 생겨서 이해는 점점 생겼으나 기본적으로 수리적 사고에 익숙하지 않다 보니, 직관화가 힘들었고, 이해해 가는 속도가 정말 느렸다.

