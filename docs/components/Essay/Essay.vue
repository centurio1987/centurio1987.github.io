업스트림에서 목표가 떨어지면, 단-중-장기 전략을 세우고, 전략 포트폴리오와 헷지 전략도 고려 했다. 실무 이해도를 바탕으로, 권한이 있는 부분은 실행 전략과 시범을 통해 실무진에게 꼼꼼히 공유 했고, 권한이 없는 부분은, 담당자와 의견을 주고 받으며 가치 사슬 안에서 최적의 시너지가 날 수 있도록 하여, 경영진에게는 요구한 결과, 실무진에게는 성장과 실무의 기회, 그리고 동기 부여, 조직에게는 더 나은 품질의 결과물을 기대하게 만드는 프로세스를 남겼다.

