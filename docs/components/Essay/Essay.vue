“이 관리 데이터를 수정하면, 기존 LCA 프로젝트가 터져요.”

“아 또 그 문제네요.”

당초 LCA 분석 서비스는 기술 기획에서, 프로젝트를 생성한 후, 그 안에서 작성-수정-완료가 이루어지도록 정했다. 그러나 한 고객사에서, 고객사 업무 형태 기준의 요구사항을 단발적이지만 지속적으로 요구 했다. 그리고, 요구사항을 반영하는 과정에서, 경향상, 프로젝트가 아니라 사업장 단위에서 LCA 분석 사항을 관리하고자 함을 나중에 파악 했다. 그러다 보니, 프로젝트의 상태와 프로젝트 외부에서 관리되는 데이터 사이에서, 컨셉 충돌에 기인한 버그가 자주 발생 했고, 모순 사항이었기 때문에, 문제가 해결에 완전히 수렴하지 못하였다.

이 사례는 제품팀이 겪고 있는 대부분의 문제를 내포하는 현상이다. 이 현상이 어떤 문제의 실들로 엮어 있는지 돞아보겠다. 제품 기획 부재다. 현상은 이미 여러 문제를 담고 있는 복마전의 표상이나, 기획 부재가 문제의 출발점임은 명료하다. 우선, 회사 차원에서 정의한 명제는 LCA 분석 SaaS였다. 그렇다면 보편적 원형이 존재해야 한다. 하지만, 명확한 목적, 목표, 범위, 컨셉, 가정을 포함한 기획이 없었고, 그러다 보니, 다운스트림에서 구현을 위한 여러 전제를 깔고 작업을 진행했다. 그리고 이렇게 완성된 서비스를 어떻게 비즈니스를 전개할지에 대한 전략도 부재했다. 오로지 고객에 달렸다는 대답만 들을 수 있었다. 그러다보니, 계약에 있어서도, 제품의 위상은 모호했다. 서비스 제공의 범위나 요구사항은 물론이고, 독립적인 솔루션인지, 기존 제품으로서 제공하는 것인지도 모호했다.

이미 구도가 그렇게 잡히고 나니, 요구사항을 여과 없이 받아들이게 되고, 사실상 솔루션처럼 흘러가게 되었다. 처음부터 명확하게 솔루션이었으면, 출발 단계부터 다르게 접근할 수 있었을 것이다. contextual inquiry 같은 리서치부터 시작해서, 별도의 제품 기획을 하고, 그 사항들이 계약에 명시가 된다면, 요구사항이 다소 튀더라도 수습이 어렵지 않았을 것이다. SaaS에 커스텀 기능을 제공하는 것으로 접근 했다면, 기존 서비스에서 확장 가능한 요구사항을 조율하고, 제품의 컨셉 범위를 넘어 간다면, 솔루션 계약으로 전환 하든, 계약을 진행하지 않든 할 수 있었을 것이다. 사실 이 조차도, 개발이 아닌 지점에 인력과 시간이 투입되는 것을 회사에서 꺼려했으므로, 풀어야 할 실타래가 그리 단순하진 않다 하겠다.

VOC가 제품 수준 요구사항으로 정제됨 없이 즉시 구현으로 넘어 가는 것도 문제의 소지가 되었다. 이미 제품 정의 및 가정과 같은 사전 명제들이 부재하여 이 또한 예상된 결과이기는 했다. 중간 과정이 없으니, 구현 과정에서 임의의 가정을 넣는 것 말고는 안전 장치가 없었다. 또한, VOC가 동시 다발적이며 즉각적 반영을 추구하다 보니, 확장 가능한 요소인지, 아니라면, 어떤 책임 대상을 교체 해야 하는지에 대한 판단의 모호성 앞에서 고민할 여지가 없었고, 변경으로 대응하게 되는 사례들이 생기게 되었다. 항상 타이트하게 작업 해야 하다 보니, 위험 요소들을 수습할 리팩토링도 충분히 진행하지 못했다. 

또한, 여러 다른 소형 프로젝트나 업무가 동시다발적으로 전개 되다 보니, 팀원들이 프론트나 백엔드 같은 기능적 역할만을 해서는 감당하기 어려운 국면으로 흘러갔다. 그래서 나는 각자가 프로덕트 엔지니어가 될 수 있기를 원했고, 기획부터 엔지니어링 전반에 이르기까지, 성장을 지도하고, 업무를 맡겼다. 당연히, 기존에 해오던 업무에 비해 알아야 할 지식과 기술이 많이 늘어 나고, 성격이 다른 업무를 복합적으로 해내야 했기에, 퍼포먼스를 끌어올리기 어려웠다. 그리고 예전 처럼 하나의 구현에 대해서 모두가 리뷰를 보거나, 내가 모든 맥락상을 추적해서 직접 전략을 짜거나, 팀원의 전략을 검토해 주기 어려운 상황이 되었다.

그러다보니 종종 문제가 수렴하지 않는 현상이 생겼다. 첫 프로젝트 때부터 구축해서 성숙도를 높여 오던 거버넌스와 프로세스가 정착되지 않았다면, 제품 resilience가 균형을 잃고, 문제가 무한 발산하며 수습 불가의 국면으로 치달았을 것이다.

---

회사는 제품을 성공의 가장 키포인트로 생각했다. 하지만, 공정의 과정이나 결과의 품질이 일정 수준으로 올라오지 않으면, 제품 주도의 성공은 너무도 요원해 보였다. 그리고, 나는 팀원들의 노력 과정도 소모적이고, 저품질의 결과물로 귀결됨으로 인해, 동력을 잃어버리게 되는 것도 우려스러웠다. 그래서 케케묵은 문제들을 해결하고자 칼을 빼들었다. 그 동안은 책임을 벗어나는 영역에 대해서는 의견을 드리는 선에 그쳤다. 그렇게 해결이 되었다면 좋았겠지만, 이런 저런 사정으로, 개선이 되지는 못하였다. 그래서 내가 직접 책임을 가지는 것이 더 좋은 해결책이란 생각이 들었다. 대표님, 이사님과 담판을 지었고, 내가 PO의 역할을 받아 오는 것으로 담판을 지었다.

직후, 나는 상황에 대한 전면 진단에 들어갔다. 앞서 이야기한 문제를을 파악하는 것에서 부터 시작하고자 했다. VOC→구현 업무, 그리고 사업 개발을 위해 경영진에서 직접 진행 중인, 동시 다발적 MVP 작업에 개입했다. LCA 분석 서비스의 제품 정의는 역정의 하기에는 멀리 왔다고 생각이 들어서, 진행해야 하는 VOC에 대해서 만이라도 3pager를 작성 했고, 팀원들에게도 작성 방법을 알려주고 숙련도가 높아질 때까지 검토를 진행 했다.

VOC나 사업 명제에서 진짜 문제를 추론하여 정의로 전환하고, 제품 컨셉을 포함한 디자인을 작성헀다. 여전히 작업 템포를 늦출 수는 없기 때문에, 최대한 컴팩트하게 진행했다. 어떤 분석 도구들이나 절차를 생략하고, 개연성과 정합성에만 집중하며 결과물을 뽑아 냈다. 머리 속에서 시뮬레이션 하며 시나리오를 쓰고, 가정을 추가해 나갔으며, 정적으로 보여 주기 위한 IA를 작성했다. 이를 토대로 디자이너 및 개발자와 논의 하여, 이후 작업이 이루어 질 수 있도록 했다.

리펙토링 없이 많은 변경과 확장이 누적된 LCA 분석 서비스는 제로부터 다시 고민해야 한다고 판단했다. 조사부터 다시 시작했다. 컨설팅, 세일즈, 마케팅 등 고객 접점에 있는 모든 팀원들과 인터뷰를 진행 했고, 가능한 알 수 있는 거시적, 미시적 환경 요소를 검토 했다. 트레이드 오프를 고려 했을 때, 리팩토링보다 제품을 새롭게 정의하고 제작하는게 좋을 것이란 결론을 내렸다. 리서치를 토대로 PMF를 충족하는 가치 제안을 찾아 제품화 전략을 구성했다. 시장의 규모나 여러 요소들 때문에, 당장의 마켓 핏을 찾기는 어려웠다. 하지만 일정 규모는 확보할 수 있을 거라 생각했고, 다음 발판을 위한 제품 전략으로써 정의 했다. 기술적 노하우를 통한 응용, 혹은, 완전히 다른 도메인이라도 문제 영역이 비슷한 다른 도메인을 공략할 수 있을거라는 판단이었다. 

그리고는 제품 컨셉을 디자인 하기 시작했다. 인터뷰를 통해 식별한 컨설턴트들의 업무 프로세스를 분석하고 문제와 솔루션을 기반으로 외삽해보며 코어가 될 제품과 지원 제품을 식별했다. 발생 가능한 많은 시나리오를 스토리 보드로 시뮬레이션 해가며, 컨셉의 정의와 가정을 조정하여 해상도를 높여 나갔다. 

제품 전략도 제대로 관리하고자 했다. 경영진, 비즈니스 영역에 계신분들께 물어보고, 단서들을 정리해서, 경영, 비즈니스 레벨 전략을 추론하고, 그에 맞게 제품팀의 전략을 정리 했다. 포트폴리오 단위로 구성하거나, 단,중,장기, 그래프 구조로 전략의 상하 관계와 의존관계를 고려 해서 정리 했다. 그런 구조로, 목표가 단번에 공략 가능하지 않다고 판단하면, 복합적인 전략을 구성했다. LCA 분석 서비스 재개발도 그 일환이었다. 전략에는 제품 뿐만 아니라, 일반 전략과 기술 전략도 포함됐다. 기술 전략은 앞으로 구현하게 될 제품이나 인프라를 예측하여, 기술 역량과 기술 자산을 마련하기 위한 전략이다.

