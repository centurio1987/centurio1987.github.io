하지만 세상이 그리 호락호락하지만은 않다는 것을, 당시의 어리석은 나는 몰랐다. 가끔은 선의가 악의가 되어 돌아오기도 하고, 생각 없이 한 배려가 누군가에겐 차별로 보일 수 있으며, 아니 뗀 굴뚝에 연기가 나기도 했다. 누군가는 편익을 위해 전략적으로 호의를 이용하기도 했다. 노력해도 친분이 더 생기지 않을 수  있다는 것도 이 때 알았다. 이런 것들은 내 머릿속 세상에서는 납득하기 어려운 것이었고, 나를 점점 불안하게 만들고 마음에 상처를 새겼지만, 어느 순간부터는 스스로가 스스로를 파괴하는 지경에 이르렀다. 당시만 해도 그들을 원망했으나, 지금은 나의 잘못이란 걸 안다. 사회란 본디 아름답기만 한건 아니기 때문에, 어떤 상황에서도 능숙하게 대처하는 것이 사회적 역량일 것이다. 하지만 나는 크고 작은 악의에 모두 크게 당황했고, 격노하거나, 외면하거나, 적대했다. 모두가 친구가 될 수는 없지만, 그렇다고 친구 아닌 자를 모두 적대할 필요는 없다. 사회 생활이란 그런 것인데, 그걸 이해하기에 당시 나는 너무 어리숙했다.

