30이란 숫자가 충분히 익숙해 질 즈음, 여전히 이런 저런 합리화를 하며 적극적으로 구직활동을 하지 않는 스스로에게 진절머리가 나기 시작했다. 이제는 그 어떤 이유도 내가 사회 생활을 하지 않을 명분은 되지 않는다고 생각이 들었다.

처음 그 날 처럼, 다시 한 번 나를 진실의 눈으로 바라보고, 전략을 짜기 시작했다. 약간의 공백과 데이터 분석가 스탠스 변경으로 인해 당장 어필할 뾰족한 기술의 부재, 하나를 깊이 파기 보다 두루 널리 아는 것으로 경쟁력을 쌓고 싶어 하는 방향성, 가치 발견에서 제공까지, 회사 경영과 비즈니스 실무 영역을 두루 경험하고 기여하고 싶어하는 성향을 고려했다. pre-A 이하의 스타트업을 타겟으로 정했고, 포지션에는 구애 받지 않기로 정했다. 당시엔 스타트업 열풍이기도 했으므로, 회사의 문화나 일하는 방식을 확인하고 선택할 수 있는 여지도 있을 것으로 생각 했다.

결론 부터 이야기 하자면, 내가 목표 했던 직장들에서의 과정은 쉽지 않았다. 주먹구구식으로, 혹은, 그날의 감정에 따라, 외부인의 목소리에 따라 의사결정이 이루어 지고, 아이디어를 내는 것이 일의 거의 대부분이라 여겨졌다. 그 마저도, 도메인 전문성이 없어서, 문장 자체로 모순 적이거나 너무 성긴 경우가 많았다. 아이디어가 결정 되면 2~4주 안에 mvp가 나와서 서비스가 이루어져야 했다. 당연히 환경은 고려되지 않았다. 

