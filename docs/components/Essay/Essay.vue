‘반복되는 상황에서도 똑같이 빛을 못 보는 건 결국 나의 역량 문제다.’

‘환경을 탓하는 것은 3류의 자세다. 그 어떤 순간에도 어떻게 하는게 최선인지 답을 찾아야 해.”

실로 그러했다. 불평은 회고도, 진단도 아니다. 물론, 상황 진단은 필요하지만, 거기서 머무르고, 부적 감정을 투사하는 것은 불평에 불과하다. 상황을 받아들이고, 그에 걸맞는 전략을 짜고, 액션 플랜을 만드는 것, 그것이 프로의 자세였다.

비단, 자세 만의 문제는 아니다. 모든 것이 완전한 상황에서 이론을 발휘하는 것은 누구나 따라할 수 있다. 위험요소와 변수를 파악하고 그에 맞는 전략을 짜는 것이 진짜 실력이라 할 수 있다. 

결국, 상황에 대한 불만과 분노는 나에 대한 것이었다. 내 실력 부족이고, 태도 부족이었다. 심지어 그것을 스스로가 아닌, 환경으로 돌린 것은 일종의 현실 도피이자 방어기제였던 셈이다. 이 시점에 와서는 이런 깨달음 하나에 창피해하고 자괴감이 들거나 하는 일희일비는 하지 않았다. 이미 수 많은 생채기가 나고 아물며, 동료의 시체가 생기면 넘고, 준비되지 않은 전투에서 적을 맞이 한지 많은 시일히 흐른 뒤였기 때문이다.

그 뒤로 내 태도는 더욱 단단해 졌다. 내 앞에서 강의 하는 교수님을 이기겠다고 호기롭게 덤비던 하룻강아지 시절이 생각 났다. 

